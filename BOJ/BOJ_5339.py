# 파이썬에서 ''로 감싸진 문자열 안에서 '를 쓰기 위해서는
# \'로 표기하는 방법이 있다.
# 따라서 ''로 감싸진 문자열 안에서 사용하는 \'는 그냥 '로 나타난다.
# 그래서 문자열 마지막에 \를 사용해주고 싶어서 그냥 저것만 적으면 마지막의 '와 \이 합쳐져 '로 표기가 되고 문자열의 마지막에 '을 찍어주지 않은 것처럼 되어
# 문법 오류가 발생한다.
# 이런 경우에는 \\를 두번 써주면 그냥 \로 사용하겠다는 뜻이 되어 원하는 결과를 얻을 수 있다.

print('     /~\\')
print('    ( oo|')
print('    _\=/_')
print('   /  _  \\')
print('  //|/.\|\\\\')
print(' ||  \ /  ||')
print('============')
print('|          |')
print('|          |')
print('|          |')